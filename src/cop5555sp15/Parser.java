public class Parser {

	List<SyntaxException> exceptionList = new ArrayList<SyntaxException>();
	
	public Program parse() {
			Program p = null;
			try {
				p = Program();
				if (p != null)
					match(EOF);
			} catch (SyntaxException e) {
				exceptionList.add(e);
			}
			if (exceptionList.isEmpty())
				return p;
			else
				return null;
	}

}
